public class Sofer {

    /*Definiți clasa Sofer care să fie caracterizată prin nume,
prenume, vârstă, număr permis de conducere. */

    private String nume;
    private String prenume;
    private int varsta;
    private long numarPermisConducere;

    public Sofer(String nume, String prenume, int varsta, long numarPermisConducere) {
        this.nume = nume;
        this.prenume = prenume;
        this.varsta = varsta;
        this.numarPermisConducere = numarPermisConducere;
    }



}
